package JavaProgram;

public class forloop_1 {

	public static void main(String[] args) {
		for(int x = 0; x < 3; x++) 
		{

			int y = -1; // y is initialized each time block is entered prints -1

			System.out.println("y is: " + y); // this always

			y = 100;

			System.out.println("y is now: " + y);

			}
	}

}
